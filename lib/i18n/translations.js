export function phraseLookupKey(value) {
  return `${value || ''}`.trim().replace(/\s+/g, ' ');
}

export function localized(value, language = defaultLanguage) {
  if (value && typeof value === 'object' && !Array.isArray(value)) {
    if (value[language]) return value[language];
    if (value[defaultLanguage]) {
      if (process.env.NODE_ENV !== 'production') {
        console.warn(`Missing localized value for language "${language}"; falling back to default language.`);
      }
      return value[defaultLanguage];
    }
    const fallback = Object.values(value).find((entry) => typeof entry === 'string');
    if (fallback) return fallback;
  }
  return value;
}

export function translatePhrase(value, language = defaultLanguage) {
  const resolved = localized(value, language);
  if (typeof resolved !== 'string' || language === defaultLanguage) return resolved;
  const dictionary = phraseTranslations[language] || {};
  return dictionary[phraseLookupKey(resolved)] || dictionary[resolved.trim()] || resolved;
}

